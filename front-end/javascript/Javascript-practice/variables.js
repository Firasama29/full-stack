//practising variables

/* VARIABLE RULES
    - variables can begin with an underscore or dollar sign
    - can only contain letters, numbers, underscores or dollar signs.
    - names are case-sensitive
    - cannot use reserved keywords
*/
var firstVariable = 'halloooo';
console.log(firstVariable);
console.log("type: " + typeof firstVariable);

var age = 30
console.log(age);
console.log("type: " + typeof age)

var decimalVar = 80.01;
console.log(decimalVar);
console.log("type: " + typeof decimalVar);


var isFriday = true
console.log(isFriday);
console.log("type: " + typeof isFriday)

var date = new Date(2021, 9, 16);
console.log(date);
console.log("type: " + typeof date)

var letters = {};

var empty = undefined;
console.log(empty);
console.log("type: " + typeof empty)

